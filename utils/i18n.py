_lang = None


def detect_system_language():
    try:
        loc = _locale.getdefaultlocale()[0] or ''
    except Exception:
        loc = ''
    loc = loc.lower()
    if loc.startswith('pt'):
        return 'pt'
    if loc.startswith('es'):
        return 'es'
    if loc.startswith('en'):
        return 'en'
    return 'pt'


def get_language():
    global _lang
    if _lang is None:
        cfg_lang = config.get('i18n', 'language', default='auto')
        _lang = detect_system_language() if cfg_lang == 'auto' else cfg_lang
        if _lang not in STRINGS:
            _lang = 'pt'
    return _lang


def set_language(lang):
    global _lang
    if lang in STRINGS or lang == 'auto':
        config.set('i18n', 'language', lang)
        _lang = None
        get_language()


def t(key):
    """Translate a key. Returns the key itself if missing."""
    lang = get_language()
    return STRINGS.get(lang, STRINGS['pt']).get(key) or STRINGS['pt'].get(key) or key


def available_languages():
    return ['auto', 'pt', 'en', 'es']
